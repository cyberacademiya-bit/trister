# ─────────────────────────── СБОРКА ДОКУМЕНТА ───────────────────────────

def set_base_style(doc):
    style = doc.styles["Normal"]
    style.font.name = "Calibri"
    style.font.size = Pt(12)

def add_page_number_footer(doc):
    for section in doc.sections:
        footer = section.footer
        p = footer.paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p.add_run()
        fldSimple = OxmlElement('w:fldSimple')
        fldSimple.set(qn('w:instr'), 'PAGE')
        run._r.append(fldSimple)

def build():
    doc = Document()
    set_base_style(doc)

    # ── Титульный лист ──
    t = doc.add_paragraph(); t.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for _ in range(6): doc.add_paragraph()
    title = doc.add_paragraph(); title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = title.add_run("ФОНДОВЫЙ РЫНОК\nОТ А ДО Я"); r.bold = True; r.font.size = Pt(36)
    sub = doc.add_paragraph(); sub.alignment = WD_ALIGN_PARAGRAPH.CENTER
    rs = sub.add_run("Полный справочник для начинающего инвестора и трейдера\nсо всеми ключевыми терминами")
    rs.italic = True; rs.font.size = Pt(16)
    for _ in range(10): doc.add_paragraph()
    foot = doc.add_paragraph(); foot.alignment = WD_ALIGN_PARAGRAPH.CENTER
    foot.add_run("Проект «Тристер»").font.size = Pt(12)
    doc.add_page_break()

    # ── Оглавление ──
    doc.add_heading("Содержание", level=1)
    for i, (title, _) in enumerate(CHAPTERS, 1):
        doc.add_paragraph(f"{i}. {title}", style="List Number" if False else None)
    doc.add_paragraph(f"{len(CHAPTERS)+1}. Глоссарий терминов")
    doc.add_page_break()

    # ── Главы ──
    for i, (title, blocks) in enumerate(CHAPTERS, 1):
        doc.add_heading(f"{i}. {title}", level=1)
        for block in blocks:
            kind = block[0]
            if kind == "p":
                doc.add_paragraph(block[1])
            elif kind == "h2":
                doc.add_heading(block[1], level=2)
            elif kind == "b":
                for item in block[1]:
                    doc.add_paragraph(item, style="List Bullet")
            elif kind == "t":
                headers, rows = block[1], block[2]
                table = doc.add_table(rows=1, cols=len(headers))
                table.style = "Light Grid Accent 1"
                for j, h in enumerate(headers):
                    cell = table.rows[0].cells[j]
                    cell.paragraphs[0].add_run(h).bold = True
                for row in rows:
                    cells = table.add_row().cells
                    for j, val in enumerate(row):
                        cells[j].text = str(val)
                doc.add_paragraph()
        doc.add_page_break()

    # ── Глоссарий ──
    doc.add_heading(f"{len(CHAPTERS)+1}. Глоссарий терминов", level=1)
    doc.add_paragraph("Алфавитный словарь основных понятий фондового рынка и трейдинга.")
    table = doc.add_table(rows=1, cols=2)
    table.style = "Light Grid Accent 1"
    table.rows[0].cells[0].paragraphs[0].add_run("Термин").bold = True
    table.rows[0].cells[1].paragraphs[0].add_run("Определение").bold = True
    for term, definition in sorted(GLOSSARY, key=lambda x: x[0].lower()):
        cells = table.add_row().cells
        cells[0].paragraphs[0].add_run(term).bold = True
        cells[1].text = definition

    add_page_number_footer(doc)
    doc.save(OUT)

    # оценка объёма и страниц (приблизительно)
    words = 0
    pages = 3  # титул + оглавление
    for _, blocks in CHAPTERS:
        cw = 0
        rows = 0
        for b in blocks:
            if b[0] in ("p", "h2"): cw += len(b[1].split())
            elif b[0] == "b": cw += sum(len(x.split()) for x in b[1]) + len(b[1]) * 4
            elif b[0] == "t":
                cw += sum(len(str(c).split()) for r in b[2] for c in r)
                rows += len(b[2]) + 1
        words += cw
        pages += 1 + cw / 380 + rows * 0.05  # каждая глава с новой страницы
    gl_words = sum(len(d.split()) + len(t.split()) for t, d in GLOSSARY)
    words += gl_words
    pages += 1 + len(GLOSSARY) / 26  # глоссарий-таблица
    return words, round(pages)

if __name__ == "__main__":
    words, pages = build()
    print(f"✅ Файл создан: {OUT}")
    print(f"   Глав: {len(CHAPTERS)} | терминов в глоссарии: {len(GLOSSARY)}")
    print(f"   Слов: ~{words} | оценка страниц: ~{pages}")
